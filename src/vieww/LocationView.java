    private void cmbKategoriSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbKategoriSearchActionPerformed
        // TODO add your handling code here:
        if (cmbKategoriSearch.getSelectedItem().equals("Location Id")) {
            txtKategori.setText("location_id");
        } else if (cmbKategoriSearch.getSelectedItem().equals("Street Address")) {
            txtKategori.setText("street_address");
        } else if (cmbKategoriSearch.getSelectedItem().equals("Postal Code")) {
            txtKategori.setText("postal_code");
        } else if (cmbKategoriSearch.getSelectedItem().equals("City")) {
            txtKategori.setText("city");
        } else if (cmbKategoriSearch.getSelectedItem().equals("State Province")) {
            txtKategori.setText("state_province");
        } else if (cmbKategoriSearch.getSelectedItem().equals("Country Name")) {
            txtKategori.setText("country_id");
        }
    }//GEN-LAST:event_cmbKategoriSearchActionPerformed

